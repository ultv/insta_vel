     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $comment = new Comment();

        $comment->author_id = auth()->id();

        $comment->post_id = $id;
        $comment->text = $request->text;

        $comment->saveOrFail();

        return redirect()->route('content.index');
    }

